    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        ///Capturamos los datos
        
        String Vin=JOptionPane.showInputDialog("Ingrese el VIN de la Moto");
        String Marca=JOptionPane.showInputDialog("Ingrese la MARCA de la moto");
        String Linea=JOptionPane.showInputDialog("Ingrese la LINEA de la moto");
        int Cilindraje=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el CILINDRAJE de la moto"));
        int Modelo=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el MODELO de la moto"));
        String FechaIngreso = JOptionPane.showInputDialog("Ingrese la fecha de ingreso  (Formato: dd/MM/yyyy):" + Linea);
        float Precio=Float.parseFloat(JOptionPane.showInputDialog("Ingrese el precio"));
        
        
        
      
        ////validamos que las fechas ingresadas esten en el formato correcto

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date date = null;
        try {
            date = dateFormat.parse(FechaIngreso);
            
        } catch (ParseException e) {
            // Manejar errores de an√°lisis de fecha
            JOptionPane.showMessageDialog(null, "Formato de fecha incorrecto. Por favor, ingrese la fecha en el formato dd/MM/yyyy.", "Error", JOptionPane.ERROR_MESSAGE);

        }
        ////creamos un nuevo objeto de la clase mOTO
        
        /// y Le pasamos los valores a los metodos set de los atributos de la clase producto

        Moto Mot=new Moto();
       
        Mot.setVin(Vin);
        Mot.setMarca(Marca);
        Mot.setModelo(Modelo);
        Mot.setLinea(Linea);
        Mot.setPrecio(Precio);
        Mot.setFechaingreso(FechaIngreso);
        Mot.setCilindraje(Cilindraje);
        


///Ejecutamos el metodo que hace el guardado 

  JOptionPane.showMessageDialog(null,obj.RegistrarProducto(Mot));
        
    }//GEN-LAST:event_jButton1ActionPerformed

